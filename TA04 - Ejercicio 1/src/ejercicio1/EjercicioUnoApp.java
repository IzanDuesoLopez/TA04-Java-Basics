package ejercicio1;

public class EjercicioUnoApp {

	public static void main(String[] args) {

		int a = 10;
		int b = 5;
		int resultado = 0;

		resultado = a + b; // Calculamos la suma
		System.out.println("La suma de A + B = " + resultado); // Mostramos la suma por consola

		resultado = a - b; // Calculamos la resta
		System.out.println("La resta de A - B = " + resultado); // Mostramos la resta por consola

		resultado = a * b; // Calculamos la multiplicación
		System.out.println("La multiplicación de A * B = " + resultado); // Mostramos la multiplicación por consola

		resultado = a / b; // Calculamos la división
		System.out.println("La división de A / B = " + resultado); // Mostramos la multiplicación por consola

		resultado = a % b; // Calculamos el módulo
		System.out.println("El módulo de A % B = " + resultado); // Mostramos el módulo por consola

	}
}
