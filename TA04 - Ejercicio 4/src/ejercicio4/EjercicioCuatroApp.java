package ejercicio4;

public class EjercicioCuatroApp {

	public static void main(String[] args) {

		int n = 100;
		int resultado = 0;

		System.out.println("Valor inicial de N = " + n);

		resultado = n + 77;
		System.out.println("N + 77 = " + resultado); // Mostramos por consola el resultado de N + 77

		resultado = n - 3;
		System.out.println("N - 3 = " + resultado); // Mostramos por consola el resultado de N - 3

		resultado = n * 2;
		System.out.println("N * 2 = " + resultado); // Mostramos por consola el resultado de N * 2
	}

}
